-- Inserting Data


-- The "formula":

    INSERT INTO table_name(column_name) VALUES (data);

-- For example:

    INSERT INTO cats(name, age) VALUES ('Jetson', 7);

-- MULTIPLE INSERT :
    INSERT INTO table_name 
                (column_name, column_name) 
    VALUES      (value, value), 
                (value, value), 
                (value, value);